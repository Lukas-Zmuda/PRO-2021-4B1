
public class Operatory {

	public static void main(String[] args) {
		
		System.out.println(10 + 5);
		System.out.println(10 - 5);
		System.out.println(10 / 3.0);
		System.out.println(10 * 5);
		System.out.println(10 % 8);
		
		System.out.println(10 < 5);
		System.out.println(10 > 5);
		System.out.println(10 <= 3.0);
		System.out.println(10 >=5);
		System.out.println(10 == 8);
		System.out.println(10 != 8);
		

	}

}
